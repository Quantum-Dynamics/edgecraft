h = 6.62607015e-34
"""Plank constant  (m^2 kg / s)."""

c = 2.99792458e8
"""Speed of light (m / s)."""

e = 1.60217663e-19
"""Elementary charge (C)."""

hc = h * c
"""Product of Plank constant and speed of light."""

magnetic_flux_quantum = h / e
"""Magnetic flux quantum."""

klitzing_constant = h / e**2
"""Klitzing constant."""
